    public void atualizaPontos(int n, int pontosGanhos){
        if(n == 1){
            atualizaPontosPlayer1(pontosGanhos);
        }
        else{
            atualizaPontosPlayer2(pontosGanhos);
        }
    }
    public void atualizaPontosPlayer1(int pontosGanhos){
        int s = (int) Integer.valueOf(this. JLabelPointsPlayer1.getText())+pontosGanhos;
        JLabelPointsPlayer1.setText(s+"");
        atualizaPlacar();
    }
    public void atualizaPontosPlayer2(int pontosGanhos){
        int s = (int) Integer.valueOf(this. JLabelPointsPlayer2.getText())+pontosGanhos;
        JLabelPointsPlayer2.setText(s+"");
        atualizaPlacar();
    }
    public void atualizaPlacar(){
        if(Integer.valueOf(this. JLabelPointsPlayer1.getText()) >Integer.valueOf(this. JLabelPointsPlayer2.getText())){
            JPanelContainerPointsPlayer1.setColor(Color.BLUE);
            JPanelContainerPointsPlayer1.setBorderColor(Color.BLUE);
            JPanelContainerPointsPlayer2.setColor(new Color(128,128,128));
            JPanelContainerPointsPlayer2.setBorderColor(new Color(128,128,128));
        }
        if(Integer.valueOf(this. JLabelPointsPlayer2.getText()) >Integer.valueOf(this. JLabelPointsPlayer1.getText())){
            JPanelContainerPointsPlayer1.setColor(new Color(128,128,128));
            JPanelContainerPointsPlayer1.setBorderColor(new Color(128,128,128));
            JPanelContainerPointsPlayer2.setColor(Color.BLUE);
            JPanelContainerPointsPlayer2.setBorderColor(Color.BLUE);
            
        }
        if(Objects.equals(Integer.valueOf(this. JLabelPointsPlayer1.getText()), Integer.valueOf(this. JLabelPointsPlayer2.getText()))){
            JPanelContainerPointsPlayer1.setColor(new Color(128,128,128));
            JPanelContainerPointsPlayer1.setBorderColor(new Color(128,128,128));
            JPanelContainerPointsPlayer2.setColor(new Color(128,128,128));
            JPanelContainerPointsPlayer2.setBorderColor(new Color(128,128,128));
        }
        
    }
