    @Override
    public void keyPressed(KeyEvent e) {
        int keyCode = e.getKeyCode();
        switch (keyCode) {
            case KeyEvent.VK_UP:
                tetrisGame2.rotate();   
                tetrisGame2.repaint();
                break;
            case KeyEvent.VK_DOWN:
                tetrisGame2.moveDown();
                tetrisGame2.repaint();
                break;
            case KeyEvent.VK_LEFT:
                tetrisGame2.moveLeft();
                tetrisGame1.repaint();
                break;
            case KeyEvent.VK_RIGHT:
                tetrisGame2.moveRight();
                tetrisGame2.repaint();
                break;
            case KeyEvent.VK_SPACE:
                tetrisGame2.fallDown();
                tetrisGame2.repaint();
                break;
            case KeyEvent.VK_W:
                tetrisGame1.rotate();
                tetrisGame1.repaint();
                break;
            case KeyEvent.VK_S:
                tetrisGame1.moveDown();
                tetrisGame1.repaint();
                break;
            case KeyEvent.VK_D:
                tetrisGame1.moveRight();
                tetrisGame1.repaint();
                break;
            case KeyEvent.VK_A:
                tetrisGame1.moveLeft();
                tetrisGame1.repaint();
                break;
            case KeyEvent.VK_Q:
                tetrisGame1.fallDown();
                tetrisGame1.repaint();
                break;
            default:
                break;
        }
                    
    }
